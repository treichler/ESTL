#include "ESTL_Types.h"


inline bool_t ValueInRange(int16_t value, range_t range)
{
  return ((value >= range.min) && (value <= range.max)) ? TRUE : FALSE;
}


inline int16_t q15_GetMantissa(q15_t q15)
{
  return (int16_t)(q15 >> 16);
}


inline uint16_t q15_GetFraction(q15_t q15)
{
  return (uint16_t)(q15 & 0xFFFF);
}
